export function getDiagnosisType(score: AxisScore): DiagnosisType {
  const { execution, strategy, interpersonal, expertise, leadership, adaptability } = score;
  const values = [execution, strategy, interpersonal, expertise, leadership, adaptability];
  const avg = values.reduce((a, b) => a + b, 0) / 6;
  const stdDev = Math.sqrt(values.reduce((sum, v) => sum + Math.pow(v - avg, 2), 0) / 6);

  // 準備中: 平均スコアが低い
  if (avg < 35) return "準備中";

  // 指揮官: リーダーシップ + 実行力が高い
  if (leadership >= 65 && execution >= 60) return "指揮官";

  // 参謀: 戦略思考 + 専門性が高い
  if (strategy >= 65 && expertise >= 60) return "参謀";

  // 外交官: 対人力 + 適応力が高い
  if (interpersonal >= 65 && adaptability >= 60) return "外交官";

  // スペシャリスト: 専門性が突出して高い
  if (expertise >= 70 && expertise - avg >= 18) return "スペシャリスト";

  // アナリスト: 戦略思考が高く、対人力が相対的に低い
  if (strategy >= 60 && interpersonal < 50) return "アナリスト";

  // チャレンジャー: 実行力 + 適応力が高い
  if (execution >= 60 && adaptability >= 60) return "チャレンジャー";

  // オールラウンダー: 全軸バランスが良く平均以上
  if (stdDev < 18 && avg >= 50) return "オールラウンダー";

  // デフォルト: 最高軸で判定
  const maxScore = Math.max(...values);
  if (maxScore === leadership || maxScore === execution) return "指揮官";
  if (maxScore === strategy || maxScore === expertise) return "参謀";
  if (maxScore === interpersonal || maxScore === adaptability) return "外交官";
  return "オールラウンダー";
}

export function calculateAxisPercentage(score: AxisScore): AxisPercentage {
  return {
    execution: score.execution,
    strategy: score.strategy,
    interpersonal: score.interpersonal,
    expertise: score.expertise,
    leadership: score.leadership,
    adaptability: score.adaptability,
  };
}

export function calculateSalaryProjection(
  currentSalary: number,
  consultingFit: number
): SalaryProjection {
  const fitRatio = consultingFit / 100;

  const typeMultiplier: Record<DiagnosisType, number> = {
    指揮官: 1.50,
    参謀: 1.55,
    外交官: 1.30,
    スペシャリスト: 1.40,
    オールラウンダー: 1.35,
    チャレンジャー: 1.35,
    アナリスト: 1.40,
    準備中: 0.90,
  };

  const mult = typeMultiplier[diagnosisType] ?? 1.2;

  if (consultingFit < 50) {
    return {
      current: currentSalary,
      year1: Math.round(currentSalary * 0.88),
      year3: Math.round(currentSalary * 1.00),
      year5: Math.round(currentSalary * 1.10),
      bestCase: Math.round(currentSalary * 1.25),
      currency: "万円",
    };
  }

  const year1 = Math.round(currentSalary * (1 + fitRatio * 0.12));
  const year3 = Math.round(currentSalary * (1 + fitRatio * (mult - 1) * 0.8));
  const year5 = Math.round(currentSalary * mult);
  const bestCase = Math.round(currentSalary * mult * 1.25);

  return { current: currentSalary, year1, year3, year5, bestCase, currency: "万円" };
}
