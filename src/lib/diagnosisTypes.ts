export function getDiagnosisType(
  axis1: number,
  axis2: number,
  axis3: number,
  axis4: number
): DiagnosisType {
  const a1: "S" | "E" = axis1 >= 0 ? "S" : "E";
  const a2: "D" | "R" = axis2 >= 0 ? "D" : "R";
  const a3: "L" | "X" = axis3 >= 0 ? "L" : "X";
  const a4: "A" | "B" = axis4 >= 0 ? "A" : "B";
  return `${a1}${a2}${a3}${a4}` as DiagnosisType;
}

export function calculateAxisPercentage(
  axis1: number,
  axis2: number,
  axis3: number,
  axis4: number
): AxisPercentage {
  return {
    s_percent: Math.round((axis1 + 100) / 2),
    d_percent: Math.round((axis2 + 100) / 2),
    l_percent: Math.round((axis3 + 100) / 2),
    a_percent: Math.round((axis4 + 100) / 2),
  };
}

export function calculateSalaryProjection(
  currentSalary: number,
  diagnosisType: DiagnosisType,
  consultingFit: number
) {
  const fitMultiplier = consultingFit / 100;
  const base = currentSalary;

  // 適性が低い場合（<75%）は転職リスクとして初期年収が下がる可能性あり
  // transitionRisk: fit=55%→0.20, fit=65%→0.10, fit=75%→0
  const transitionRisk = Math.max(0, 0.75 - fitMultiplier);

  const year1 = Math.round(base * (1 + 0.15 * fitMultiplier - 0.6 * transitionRisk));
  const year3 = Math.round(base * (1 + 0.45 * fitMultiplier - 0.3 * transitionRisk));
  const year5 = Math.round(base * (1 + 0.85 * fitMultiplier));
  const bestCase = Math.round(base * (1 + 1.5 * fitMultiplier));

  return {
    current: base,
    year1,
    year3,
    year5,
    bestCase,
    currency: "万円" as const,
  };
}
