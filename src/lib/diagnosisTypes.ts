export function getDiagnosisTypeName(score: RadarScore): DiagnosisTypeName {
  const avg = (score.execution + score.strategy + score.interpersonal + score.expertise + score.leadership + score.adaptability) / 6;

  // 準備中: 全体平均が低い
  if (avg < 38) return "準備中";

  // 指揮官: リーダーシップ + 実行力が高い
  if (score.leadership >= 68 && score.execution >= 62) return "指揮官";

  // 参謀: 戦略思考 + 専門性が高い
  if (score.strategy >= 68 && score.expertise >= 52) return "参謀";

  // 外交官: 対人力 + 適応力が高い
  if (score.interpersonal >= 68 && score.adaptability >= 52) return "外交官";

  // スペシャリスト: 専門性が突出
  if (score.expertise >= 68) return "スペシャリスト";

  // アナリスト: 戦略思考が高いが対人力が低め
  if (score.strategy >= 62 && score.interpersonal < 45) return "アナリスト";

  // チャレンジャー: 実行力 + 適応力が高い
  if (score.execution >= 62 && score.adaptability >= 52) return "チャレンジャー";

  // オールラウンダー: それ以外のバランス型
  return "オールラウンダー";
}

export function calculateConsultingFit(score: RadarScore): number {
  // 戦略思考・リーダーシップ・実行力を重視した重み付き平均
  const fit = Math.round(
    score.strategy * 0.25 +
    score.execution * 0.20 +
    score.leadership * 0.20 +
    score.interpersonal * 0.15 +
    score.expertise * 0.12 +
    score.adaptability * 0.08
  );
  return Math.max(0, Math.min(100, fit));
}

export function calculateSalaryProjection(
  currentSalary: number,
  consultingFit: number
): SalaryProjection {
  const fitMultiplier = consultingFit / 100;
  const base = currentSalary;

  // 適性が低い場合（fit < 75%）は転職直後に一時的な年収低下リスクあり
  const transitionRisk = Math.max(0, 0.75 - fitMultiplier);
  const year1 = Math.round(base * (1 + 0.15 * fitMultiplier - 0.6 * transitionRisk));
  const year3 = Math.round(base * (1 + 0.45 * fitMultiplier - 0.3 * transitionRisk));
  const year5 = Math.round(base * (1 + 0.85 * fitMultiplier));
  const bestCase = Math.round(base * (1 + 1.5 * fitMultiplier));

  return { current: base, year1, year3, year5, bestCase, currency: "万円" };
}
