export function getDiagnosisType(score: AxisScore): DiagnosisType {
  const { execution, strategy, interpersonal, expertise, leadership, adaptability } = score;
  const values = [execution, strategy, interpersonal, expertise, leadership, adaptability];
  const avg = values.reduce((a, b) => a + b, 0) / 6;
  const stdDev = Math.sqrt(values.reduce((sum, v) => sum + Math.pow(v - avg, 2), 0) / 6);

  // 準備中: ほぼ全問1回答相当（全1s=20点）のみ
  if (avg < 22) return "準備中";

  // 相対的な「高い」判定: 平均より margin 以上高い軸をその人の強みとみなす
  const margin = Math.max(7, avg * 0.12);
  const isHigh = (v: number) => v >= avg + margin;
  const isLow = (v: number) => v < avg - margin;

  // 指揮官: リーダーシップ + 実行力が相対的に高い
  if (isHigh(leadership) && isHigh(execution)) return "指揮官";

  // 参謀: 戦略思考 + 専門性が相対的に高い
  if (isHigh(strategy) && isHigh(expertise)) return "参謀";

  // 外交官: 対人力 + 適応力が相対的に高い
  if (isHigh(interpersonal) && isHigh(adaptability)) return "外交官";

  // スペシャリスト: 専門性が突出して高い
  if (isHigh(expertise) && expertise - avg >= 14) return "スペシャリスト";

  // アナリスト: 戦略思考が高く、対人力が相対的に低い
  if (isHigh(strategy) && isLow(interpersonal)) return "アナリスト";

  // チャレンジャー: 実行力 + 適応力が高い
  if (isHigh(execution) && isHigh(adaptability)) return "チャレンジャー";

  // 単軸優位フォールバック
  if (isHigh(leadership)) return "指揮官";
  if (isHigh(strategy)) return "アナリスト";
  if (isHigh(expertise)) return "スペシャリスト";
  if (isHigh(execution)) return "チャレンジャー";
  if (isHigh(interpersonal) || isHigh(adaptability)) return "外交官";

  // オールラウンダー: 全軸バランスが良い
  if (stdDev < avg * 0.22 && avg >= 40) return "オールラウンダー";

  // 最終フォールバック: 最も高い軸で判定
  const axisRanked = (
    [
      ["leadership", leadership], ["execution", execution],
      ["strategy", strategy], ["expertise", expertise],
      ["interpersonal", interpersonal], ["adaptability", adaptability],
    ] as [string, number][]
  ).sort((a, b) => b[1] - a[1]);

  const top = axisRanked[0][0];
  const second = axisRanked[1][0];

  if (top === "leadership" || (top === "execution" && second !== "adaptability")) return "指揮官";
  if (top === "execution" || top === "adaptability") return "チャレンジャー";
  if (top === "strategy" || top === "expertise") return "参謀";
  if (top === "interpersonal") return "外交官";
  return "オールラウンダー";
}

// 偏差値変換: raw 0-100 → 偏差値 (平均50, SD10)
// 基準: 全問3点選択 → raw=60 → 偏差値50
// 全問1点 → raw=20 → 偏差値30、全問5点 → raw=100 → 偏差値70
// 式: 偏差値 = 50 + (raw - 60) / 2
function toHensachi(raw: number): number {
  return Math.min(70, Math.max(30, Math.round(50 + (raw - 60) / 2)));
}

export function calculateAxisPercentage(score: AxisScore): AxisPercentage {
  return {
    execution: toHensachi(score.execution),
    strategy: toHensachi(score.strategy),
    interpersonal: toHensachi(score.interpersonal),
    expertise: toHensachi(score.expertise),
    leadership: toHensachi(score.leadership),
    adaptability: toHensachi(score.adaptability),
  };
}

export function calculateSalaryProjection(
  currentSalary: number,
  diagnosisType: DiagnosisType,
  consultingFit: number
): SalaryProjection {
  const fitRatio = consultingFit / 100;

  const typeMultiplier: Record<DiagnosisType, number> = {
    指揮官: 1.50,
    参謀: 1.55,
    外交官: 1.30,
    スペシャリスト: 1.40,
    オールラウンダー: 1.35,
    チャレンジャー: 1.35,
    アナリスト: 1.40,
    準備中: 0.90,
  };

  const mult = typeMultiplier[diagnosisType] ?? 1.2;

  if (consultingFit < 50) {
    return {
      current: currentSalary,
      year1: Math.round(currentSalary * 0.88),
      year3: Math.round(currentSalary * 1.00),
      year5: Math.round(currentSalary * 1.10),
      bestCase: Math.round(currentSalary * 1.25),
      currency: "万円",
    };
  }

  const year1 = Math.round(currentSalary * (1 + fitRatio * 0.12));
  const year3 = Math.round(currentSalary * (1 + fitRatio * (mult - 1) * 0.8));
  const year5 = Math.round(currentSalary * mult);
  const bestCase = Math.round(currentSalary * mult * 1.25);

  return { current: currentSalary, year1, year3, year5, bestCase, currency: "万円" };
}
